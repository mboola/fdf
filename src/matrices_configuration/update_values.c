#include "ft_fdf.h"

void	update_angle(double *angle, double increment)
{
	int	tmp;

	printf("%1f\n", *angle);

	if (*angle + increment > MAX_ANGLE)
	{
		tmp = MAX_ANGLE - *angle;
		*angle = increment - tmp;
	}
	else
		*angle += increment;
}
